pub fn build_programmer_vocabulary_create_payload(prefix: &str) -> Value {
    json!({
        "model": "speech-biasing",
        "input": {
            "action": "create_vocabulary",
            "target_model": "paraformer-realtime-v2",
            "prefix": prefix,
            "vocabulary": PROGRAMMER_VOCABULARY.iter().map(|(text, weight, lang)| json!({
                "text": text,
                "weight": weight,
                "lang": lang,
            })).collect::<Vec<_>>()
        }
    })
}

#[cfg(test)]
mod tests {
    use super::build_programmer_vocabulary_create_payload;

    #[test]
    fn programmer_vocabulary_create_payload_targets_realtime_v2() {
        let payload = build_programmer_vocabulary_create_payload("progx-auto");

        assert_eq!(payload["model"].as_str(), Some("speech-biasing"));
        assert_eq!(payload["input"]["action"].as_str(), Some("create_vocabulary"));
        assert_eq!(
            payload["input"]["target_model"].as_str(),
            Some("paraformer-realtime-v2")
        );
        assert_eq!(payload["input"]["prefix"].as_str(), Some("progx-auto"));
        assert!(payload["input"]["vocabulary"].as_array().is_some_and(|items| !items.is_empty()));
    }
}
